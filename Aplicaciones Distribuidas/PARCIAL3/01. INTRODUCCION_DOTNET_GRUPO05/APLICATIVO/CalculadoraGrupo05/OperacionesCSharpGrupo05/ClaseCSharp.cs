namespace OperacionesCSharpGrupo05
{
    public class ClaseCSharp
    {
        public String sumar(double num1, double num2)
        {
            return "Suma = " + Convert.ToString(num1 + num2);
        }
        public String restar(double num1, double num2)
        {
            return "Resta = " + Convert.ToString(num1 - num2);
        }
    }
}