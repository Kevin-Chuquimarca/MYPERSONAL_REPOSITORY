public class Calculator {
    public int sumTwoValues(int a, int b) {
        return a + b;
    }

    public int subtractTwoValues(int a, int b) {
        return a - b;
    }

    public int multTwoValues(int a, int b) {
        return a * b;
    }

    public int divideTwoValues(int a, int b) {
        return a / b;
    }
}

