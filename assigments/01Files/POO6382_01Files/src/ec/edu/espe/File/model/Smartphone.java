package ec.edu.espe.File.model;

public class Smartphone {
    private int id;
    private String name;
    private String brand;
    private String color;
    private float price;

    public Smartphone(int id, String name, String brand, String color, float price) {
        this.id = id;
        this.name = name;
        this.brand = brand;
        this.color = color;
        this.price = price;
    }
}
